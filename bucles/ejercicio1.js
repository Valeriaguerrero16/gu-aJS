//Ejercicio 1.1: Imprime los números del 1 al 10 usando for.

for(let i =1; i<=10; i++ ){
    console.log(i)
}

//se utiliza un bucle FOR para imprimir los nueros del 1 al 10 en la consola.la variable "i" con el valor de 1 y si "i" es menor o igual a 10, el bucle seguira su flujo. "i" se incrementa en 1 y se imprime su valor en la consola.
