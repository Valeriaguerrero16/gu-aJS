const sumar =(a,b)=> a + b;

//El código convierte la función tradicional sumar a una arrow function. La función original sumar(a, b) retorna la suma de dos números. Al convertirla a una arrow function, se usa la sintaxis const sumar = (a, b) => a + b, donde se reemplaza la palabra clave function por const, y => se utiliza para definir la función. Además, al ser una arrow function de una sola expresión, no es necesario usar return, ya que el valor es retornado automaticamente.






